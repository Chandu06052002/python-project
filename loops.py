# FOR LOOP

for i in range(1,11):
    print(i)

fruits = ['banana','apple','grapes','pineapple']
for fruit in fruits:
    print(fruit) 


# WHILE LOOP

i = 1
while i <= 5:
    print(i)
    i+=1


# WHILE LOOP INSIDE THE FOR LOOP

for num in range(1,6):
    num2 = 1
    while num2 <= 5:
        print(num2)
        num2 +=1
    print(num)
